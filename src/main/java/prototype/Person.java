package main.java.prototype;

public abstract class Person {

    String name;
    String family;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getFamily() {
        return family;
    }

    public void setFamily(String family) {
        this.family = family;
    }

    public abstract Person clone();
}
